#!/usr/bin/env python3
"""tsumugi autorun.py tests — the kotoba-native offline beat (ADR-2606092000).

Verifies the fleet-heartbeat routine (no Claude-cloud, no network):
  - a beat appends a content-addressed tx (:tx/id :tx/prev :tx/datoms) to the local log
  - the tx records the cycle: seed counts · forage · dataset content-hash + triples · coverage
  - the chain links (beat N+1's :tx/prev == beat N's :tx/id) — durable across kills
  - determinism: same seed + same beat index → byte-identical head CID
  - the provider dataset is regenerated each beat (etzhayyim keeps FEEDING others)
"""
import sys, re, pathlib, tempfile

ACTOR_DIR = pathlib.Path(__file__).resolve().parent.parent
sys.path.insert(0, str(ACTOR_DIR / "methods"))
import autorun  # noqa: E402

PASS, FAIL = "\033[32mPASS\033[0m", "\033[31mFAIL\033[0m"
results = []

def check(name, cond):
    results.append(bool(cond))
    print(f"  [{PASS if cond else FAIL}] {name}")


def main():
    print("\n=== tsumugi autorun (kotoba-native beat) tests ===")
    with tempfile.TemporaryDirectory() as d:
        log = pathlib.Path(d) / "cyc.datoms.kotoba.edn"
        b0 = autorun.beat(log)
        b1 = autorun.beat(log)
        lines = [ln for ln in log.read_text(encoding="utf-8").splitlines() if ln.strip()]

        check("each beat appends one tx", len(lines) == 2)
        check("tx is content-addressed (:tx/id tx:sha256:…)",
              b0["cid"].startswith("tx:sha256:") and ':tx/id "tx:sha256:' in lines[0])
        check("first beat's prev is genesis", b0["prev"] == "tx:genesis")
        check("chain links: beat1.prev == beat0.id", b1["prev"] == b0["cid"])

        d0 = b0["datoms"]
        check("records seed node/edge counts", d0[":tsumugi.cycle/seed-nodes"] > 100)
        check("records dataset content-hash (provider identity)",
              d0[":tsumugi.cycle/dataset-content-hash"].startswith("sha256:"))
        check("records dataset triples + coverage + publisher DID",
              d0[":tsumugi.cycle/dataset-triples"] > 1000
              and d0[":tsumugi.cycle/published-by"] == autorun.publish.PUBLISHER_DID)
        check("records the 粘菌/菌糸 forage decision",
              "GROW" in d0[":tsumugi.cycle/forage"] or "FRUIT" in d0[":tsumugi.cycle/forage"])

        # the provider dataset was (re)generated by the beat — etzhayyim keeps feeding others
        check("beat regenerates the published linked-data dataset",
              (ACTOR_DIR / "out" / "etzhayyim-power-graph.nt").exists())

    # determinism: a fresh log's genesis beat is byte-identical (logical time, no wall clock)
    with tempfile.TemporaryDirectory() as d1, tempfile.TemporaryDirectory() as d2:
        c1 = autorun.beat(pathlib.Path(d1) / "l.edn")["cid"]
        c2 = autorun.beat(pathlib.Path(d2) / "l.edn")["cid"]
    check("determinism: identical genesis head CID", c1 == c2)

    print(f"\n{sum(results)}/{len(results)} passed")
    return 0 if all(results) else 1


if __name__ == "__main__":
    sys.exit(main())
